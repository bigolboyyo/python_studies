# Multiple Assignment = Allows us to assign multiple variables at the same time in one line of code

# Example of standard assignment
# name = "Dude"
# age = 28
# sex = False

# Using MULTIPLE ASSIGNMENT WOOOT!
name, age, sex = "Wes", 28, False

print(name)
print(age)
print(sex)

# Another example using spongebob characters
# Let's assume all of our characters are 30 years old

# Standard Assignment
# Spongebob = 30
# Patrick = 30
# Sandy = 30
# Squidward = 30

# Multiple Assignment
Spongebob = Patrick = Sandy = Squidward = 30

print(Spongebob)
print(Patrick)
print(Sandy)
print(Squidward)
